import java.util.Scanner;

class PosNeg
{
public static void main(String [] args)
{
 System.out.println("Enter any Number");
 Scanner s=new Scanner(System.in);
 int n=s.nextInt();
 
 if(n>0)
 {
	 System.out.println(+n+" is Positive");
 }
 else
 {
	 System.out.println(+n+" is Negative");
}
}
}


	 
 
